/**
 * Removes all dev entries from package-lock.json because it got huge
 */
import { readFileSync, writeFileSync } from "fs";
const pkglock = JSON.parse(readFileSync("./package-lock.json"));
for (const pkg in pkglock.packages)
  if (pkglock.packages[pkg].dev) delete pkglock.packages[pkg];
for (const dep in pkglock.dependencies)
  if (pkglock.dependencies[dep].dev) delete pkglock.dependencies[dep];
writeFileSync("./package-lock.json", JSON.stringify(pkglock, null, 2));
